                <h2 class="h1">PSA Antwerp</h2>
                <p>
                    Het verhaal van PSA Antwerp begon in 2002, met de fusie van twee gevestigde havenbedrijven, Hessenatie en 
                    Noord Natie. Deze twee bedrijven met een eeuwenlange ervaring werden samen één van de belangrijkste spelers in de 
                    Antwerpse haven. Onmiddellijk na de fusie werd de nieuwe onderneming overgenomen door PSA International, één van de 
                    grootste havengroepen ter wereld gespecialiseerd in container behandeling, met thuisbasis in Singapore.
                    <br>
                    De PSA Groep is actief in havenprojecten in Azië, Europa en Amerika, 
                    met als belangrijkste vlaggenschepen de PSA Singapore Terminals en PSA Antwerp. 
                    Als de geprefereerde havenoperator in alle wereldhavens, behandelt PSA zo'n 185.000 containers per dag, 
                    wat in 2016 neerkwam op een totaal van 67,63 miljoen TEUs (twenty foot equivalent units). 
                    De terminal in Singapore neemt van dit cijfer een kleine helft voor zijn rekening. Het Belgische PSA Antwerp, 
                    de grootste investering van PSA buiten Singapore, behandelde 10,0 miljoen TEUs in 2016 en 
                    wordt het tweede vlaggenschip van de PSA-groep genoemd.
                    <br>
                    PSA baat in Antwerpen 3 container terminals uit (MPET aan het Deurganck dok, Noordzee Terminal en Europa Terminal) 
                    en 1 multipurpose terminal (Churchill Terminal aan Churchilldok). 
                    Meer dan 80% van alle containers die Antwerpen aandoen, passeert één van deze terminals. 
                    Daarnaast is PSA in Antwerpen ook actief in de behandeling van stukgoed via de firma NHS; 
                    een joint venture tussen PSA Antwerp en de Nova Natie Group.
                    <br>
                    Vandaag kan PSA in Antwerpen bogen op de ervaring en expertise van ongeveer 3.000 personeelsleden 
                    om de complexe operaties vlot te beheren.
                    Dankzij hun inzet en motivatie draagt ook PSA Antwerp in belangrijke mate bij tot het concretiseren 
                    van de slagzin van de groep ‘PSA, The World’s Port of Call’.
